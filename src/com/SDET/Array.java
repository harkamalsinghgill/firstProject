package com.SDET;

public class Array {

    public static void main(String[] args) {


        //array is set of elements which are of same data types

        //type 1
        int marks[] = new int[5];// single dimensional array
        marks[0] = 2;
        marks[1] = 3;
        marks[2] = 4;
        marks[3] = 5;
        marks[4] = 6;

// read elements from an array
        for (int i = 0; i < 5; i++) {
            System.out.println(marks[i]);
        }


        //type 2


    }
}
